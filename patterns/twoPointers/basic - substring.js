const nums = "0123456789";

console.log(nums.substring(0));
// 0123456789

console.log(nums.substring(0,0));
// ""

console.log(nums.substring(0,1));
// 0

console.log(nums.substring(0,10));
// 0123456789

console.log(nums.substring(0,9));
// 012345678

console.log(nums.substring(9));
// 9

console.log(nums.substring(10));
// ""
